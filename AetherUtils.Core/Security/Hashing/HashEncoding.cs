namespace AetherUtils.Core.Security.Hashing
{
    /// <summary>
    /// Represents valid encoding options for hashing.
    /// </summary>
    public enum HashEncoding
    {
        /// <summary>
        /// Represents Hexadecimal encoding (BASE-16).
        /// </summary>
        Hex = 16,
        
        /// <summary>
        /// Represents BASE-64 encoding.
        /// </summary>
        Base64 = 64,
    }
}
